
def get_palette(palette_name):

    match palette_name:
        case "xp":
            return xp_colours()
        case "vista":
            return vista_colours()
        case "seven":
            return seven_colours()
        case "all":
            return xp_colours() + vista_colours() + seven_colours()
        case _:
            raise Exception(f"Unknown palette key: {palette_name}")


